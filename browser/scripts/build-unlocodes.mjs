#!/usr/bin/env node
// Regenerate _data/unlocodes.yaml (the file the site reads at build
// time via data.unlocodes):
//
//   EN names  — verbatim from the UN/LOCODE database (UNCEFACT 2025-1
//               JSON-LD vendored in the `unlocodes` gem).
//   FR names  — _data/unlocodes-i18n.yaml (curated translations).
//   EN tweaks — also in unlocodes-i18n.yaml, ONLY where the official
//               name is not display-worthy (endonyms, compounds):
//               e.g. Torino → Turin, Hakata/Fukuoka → Fukuoka.
//
// Never edit _data/unlocodes.yaml by hand — it is fully generated.
// Curation lives in _data/unlocodes-i18n.yaml only.
//
// Usage:
//   node scripts/build-unlocodes.mjs [path-to-locode.jsonld]
// Default dataset path: ~/src/mn/unlocodes/lib/unlocodes/data/locode.jsonld

import { readFileSync, writeFileSync, readdirSync, existsSync } from 'node:fs'
import { homedir } from 'node:os'
import { join } from 'node:path'
import { parse as parseYaml } from 'yaml'

const root = new URL('..', import.meta.url).pathname
const dataDirs = [join(root, '..', 'plenary'), join(root, '..', 'meetings')]
const i18nPath = join(root, '..', '_data', 'unlocodes-i18n.yaml')
const outPath = join(root, '..', '_data', 'unlocodes.yaml')
const datasetPath =
  process.argv[2] ?? join(homedir(), 'src/mn/unlocodes/lib/unlocodes/data/locode.jsonld')

// 1. Codes actually used by the data (meeting `city` + venue `unlocode`).
const codes = new Set()
for (const dir of dataDirs) {
  for (const file of readdirSync(dir)) {
    if (!file.endsWith('.yaml')) continue
    const text = readFileSync(join(dir, file), 'utf8')
    for (const match of text.matchAll(/^(?: {2})?(?:city|unlocode):\s*([A-Z0-9]{5})\s*$/gm)) {
      codes.add(match[1])
    }
  }
}

// 2. English names from the database, verbatim.
const doc = JSON.parse(readFileSync(datasetPath, 'utf8'))
const db = new Map()
for (const node of doc['@graph'] ?? []) {
  const code = node['rdf:value']
  if (typeof code !== 'string' || !codes.has(code.toUpperCase())) continue
  const labels = Array.isArray(node['rdfs:label']) ? node['rdfs:label'] : [node['rdfs:label']]
  const tagged = labels.find((l) => l && l['@language'] === 'en')
  const untagged = labels.find((l) => l && !l['@language'])
  const name = tagged?.['@value'] ?? untagged?.['@value']
  if (name) db.set(code.toUpperCase(), name)
}

// 3. Curated i18n (fr translations + rare en display overrides).
const i18n = existsSync(i18nPath) ? (parseYaml(readFileSync(i18nPath, 'utf8')) ?? {}) : {}

// 4. Emit.
const quote = (s) => `"${s.replace(/"/g, '\\"')}"`
const lines = [
  '# GENERATED by scripts/build-unlocodes.mjs — do not edit by hand.',
  '# en: UN/LOCODE database (UNCEFACT 2025-1, unlocodes gem), verbatim.',
  '# fr + en display overrides: _data/unlocodes-i18n.yaml (curated).',
]
const missing = []
for (const code of [...codes].sort()) {
  const nameEn = db.get(code)
  if (!nameEn) {
    missing.push(code)
    continue
  }
  const curated = i18n[code] ?? {}
  const en = curated.en ?? nameEn
  const fr = curated.fr ?? en
  lines.push(`${code}:`)
  lines.push(`  en: ${quote(en)}`)
  lines.push(`  fr: ${quote(fr)}`)
}
writeFileSync(outPath, `${lines.join('\n')}\n`)

console.log(`Wrote ${[...codes].length - missing.length} codes to ${outPath}`)
if (missing.length > 0) console.warn(`NOT IN DATABASE (add to unlocodes-i18n.yaml with en+fr): ${missing.join(', ')}`)
